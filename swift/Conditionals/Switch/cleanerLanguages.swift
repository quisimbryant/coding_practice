var abbreviation: String = "RUS"

switch abbreviation {
    case "RUS":
        print("Russian")
    case "ENG":
        print("English")
    case "ITA":
        print("Italian")
    case "SPA":
        print("Spanish")
    default:
        print("Abbreviation was not found.")
}